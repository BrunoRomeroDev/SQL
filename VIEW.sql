ALTER VIEW SC6010VIEW AS --CREATE VIEW SC6010VIEW
	
		SELECT C6_NUM, C6_PRODUTO,  SUM(C6_PRCVEN)AS FATOTAL, A1_COD, A1_NOME 
			FROM SC6010 INNER JOIN SA1010 ON C6_CLI = A1_COD 
				WHERE SC6010.D_E_L_E_T_ = '' AND C6_CLI = '001030' 
					GROUP BY C6_CLI,C6_PRCVEN,C6_PRODUTO, C6_NUM,A1_COD, A1_NOME HAVING C6_PRCVEN >10000

